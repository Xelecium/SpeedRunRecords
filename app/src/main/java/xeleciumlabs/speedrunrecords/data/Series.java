public class Series {


    String mSeriesId;
    String mSeriesName;
    String mSeriesWebLink;


    public String getSeriesId() {
        return mSeriesId;
    }
    public void setSeriesId(String seriesId) {
        mSeriesId = seriesId;
    }
    public String getSeriesName() {
        return mSeriesName;
    }
    public void setSeriesName(String seriesName) {
        mSeriesName = seriesName;
    }
    public String getSeriesWebLink() {
        return mSeriesWebLink;
    }
    public void setSeriesWebLink(String seriesWebLink) {
        mSeriesWebLink = seriesWebLink;
    }

}
